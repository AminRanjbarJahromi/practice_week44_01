package org.example;

public class Static_or_instatnce01 {

    private int min;
    private  int max;

    public Static_or_instatnce01(){
        this.min = min;
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }
}
